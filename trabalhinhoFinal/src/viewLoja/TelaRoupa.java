    private void BTExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTExcluirActionPerformed
        // TODO add your handling code here:
        int k = jTableRoupa.getSelectedRow();
        int cod = (int) jTableRoupa.getValueAt(k, 0);
        String cor = (String) jTableRoupa.getValueAt(k, 1);
        int peca = (int) jTableRoupa.getValueAt(k, 2);
        double preco = (double) jTableRoupa.getValueAt(k, 3);
        String categoria = (String) jTableRoupa.getValueAt(k, 4);
        int cod_fab = (int) jTableRoupa.getValueAt(k, 5);
        Connection con = null;
        try {
            con = FabricaDeConexoes.getConnection();
        } catch (Exception ex) {
            Logger.getLogger(TelaRoupa.class.getName()).log(Level.SEVERE, null, ex);
        }
        Statement stmt = null;
        try {
            stmt = con.createStatement();
        } catch (SQLException ex) {
            Logger.getLogger(TelaRoupa.class.getName()).log(Level.SEVERE, null, ex);
        }

        String sql1 = "DELETE FROM roupa WHERE codigo_da_peça = " + cod + " ";
        String sql2 = "DELETE FROM roupa WHERE cor = " + cor + " ";
        String sql3 = "DELETE FROM roupa WHERE peça = " + peca + " ";
        String sql4 = "DELETE FROM roupa WHERE preço = " + preco + " ";
        String sql5 = "DELETE FROM roupa WHERE categoria = " + categoria + " ";
        String sql6 = "DELETE FROM roupa WHERE código_do_fornecedor = " + cod_fab + " ";

        try {
            stmt.executeUpdate(sql1);
            JOptionPane.showMessageDialog(this, "Excluído com Sucesso!");
            stmt.executeUpdate(sql2);
            stmt.executeUpdate(sql3);
            stmt.executeUpdate(sql4);
            stmt.executeUpdate(sql5);
            stmt.executeUpdate(sql6);

            if (jTableRoupa.getSelectedRow() >= 0) {
                jTableRoupa.remove(jTableRoupa.getSelectedRow());
            }

        } catch (SQLException ex) {
            System.out.println("");
        }
        CodP.setText("");
        Cor.setText(String.valueOf(""));
        QTD.setText(String.valueOf(""));
        Preco.setText(String.valueOf(""));
        Categoria.setText(String.valueOf(""));
        CodFab.setText(String.valueOf(""));

    }//GEN-LAST:event_BTExcluirActionPerformed

