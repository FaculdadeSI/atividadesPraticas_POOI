    private void pesquisaItem(String tipo) throws Exception {
        DefaultTableModel modelo = (DefaultTableModel) jTableRoupa.getModel();
        modelo.setNumRows(0);
        RoupasDAO dao = new RoupasDAO();
        RoupasVO d = new RoupasVO();

        for (RoupasVO rs : dao.pesquisar(tipo)) {
            modelo.addRow(new Object[]{
                rs.getCodigo_da_peca(),
                rs.getCor(),
                rs.getQtd_peca(),
                rs.getPreco(),
                rs.getCategoria(),
                rs.getCodigo_fabricante()
            });
        }
    }

    private void carregaTabela() {
        DefaultTableModel modelo = (DefaultTableModel) jTableRoupa.getModel();
        modelo.setNumRows(0);

        jTableRoupa.getColumnModel().getColumn(0).setPreferredWidth(20);
        jTableRoupa.getColumnModel().getColumn(1).setPreferredWidth(20);
        jTableRoupa.getColumnModel().getColumn(2).setPreferredWidth(20);
        jTableRoupa.getColumnModel().getColumn(3).setPreferredWidth(20);
        jTableRoupa.getColumnModel().getColumn(4).setPreferredWidth(20);
        jTableRoupa.getColumnModel().getColumn(5).setPreferredWidth(20);

        try {
            Connection con = FabricaDeConexoes.getConnection();
            PreparedStatement pstm;
            ResultSet rs;

            pstm = con.prepareStatement("select * from roupa");
            rs = pstm.executeQuery();

            while (rs.next()) {
                modelo.addRow(new Object[]{
                    rs.getInt(1),
                    rs.getString(2),
                    rs.getInt(3),
                    rs.getDouble(4),
                    rs.getString(5),
                    rs.getInt(6)
                });
            }
        } catch (Exception ErroSQL) {
            System.out.println(" ");
        }
    }

